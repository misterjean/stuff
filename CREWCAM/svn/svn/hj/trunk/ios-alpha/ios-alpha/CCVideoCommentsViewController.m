- (IBAction)addCommentButton:(id)sender {
    
    UIAlertView * alert = [[UIAlertView alloc] initWithTitle:@"New Comment" message:@"Enter Comment" delegate:self cancelButtonTitle:@"Cancel" otherButtonTitles:@"Ok", nil];
    alert.alertViewStyle = UIAlertViewStylePlainTextInput;
    [alert show];
}

- (void)alertView:(UIAlertView *)alertView clickedButtonAtIndex:(NSInteger)buttonIndex{ 
    
    if (buttonIndex == 1)
    {
        if (![[[alertView textFieldAtIndex:0] text] isEqualToString:@""])
        {
        [video addComment:[[alertView textFieldAtIndex:0] text]];
        
        [[self tableView] reloadData];
        }
        
    }
}

